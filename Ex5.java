/*Exercício 5: Estruturas de Repetição
Escreva um programa que imprima todos os números de 1 a 10 usando um loop for.*/

public class Ex5 {
    public static void main(String[]args){

        int i;

        for (i = 1; i <= 10; i++){
            System.out.printf("%d ", i);
        }
    }
}
